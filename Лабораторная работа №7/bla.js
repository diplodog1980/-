function task3() {
    // Задание 3: Определяем, в какую четверть часа попадает число
    const min = +prompt('Введите число от 0 до 59:');  // Запрашиваем минуту у пользователя

    switch (true) {
        case (min >= 0 && min < 15):
            alert('Первая четверть');  // Если минуты от 0 до 14
            break;
        case (min >= 15 && min < 30):
            alert('Вторая четверть');  // Если минуты от 15 до 29
            break;
        case (min >= 30 && min < 45):
            alert('Третья четверть');  // Если минуты от 30 до 44
            break;
        case (min >= 45 && min < 60):
            alert('Четвертая четверть');  // Если минуты от 45 до 59
            break;
        default:
            alert('Неверное число');  // Если введено число не от 0 до 59
            break;
    }
}

function task4() {
    // Задание 4: Проверка первого символа строки и наличие номера телефона
    const str = prompt('Введите строку с цифрами');  // Запрашиваем строку с цифрами у пользователя
    const phoneNumber = '1234567890';  // Пример номера телефона

    // Проверяем, начинается ли строка с цифры 1, 2 или 3
    if (/^[123]/.test(str)) {
        alert('Первая цифра 1, 2 или 3');  // Если строка начинается с 1, 2 или 3
    } else {
        alert('Нет');  // Если строка не начинается с 1, 2 или 3
    }

    // Проверяем, содержит ли строка номер телефона
    if (str.includes(phoneNumber)) {
        alert('Ваш номер телефона найден в строке');  // Если номер телефона найден
    } else {
        alert('Ваш номер телефона не найден в строке');  // Если номер телефона не найден
    }
}

